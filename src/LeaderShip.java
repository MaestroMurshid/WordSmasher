    public void FetchPlayer()
    {
        try{
            pst = conn.prepareStatement("select player_name as 'Player Name',game_mode as 'Game Mode',correct_words as 'Correct Words',wrong_words as 'Wrong Words',missed_words as 'Missed Words',score as 'Score' from player_details where id!='' order by score desc ");
            rs=pst.executeQuery();
            sc_table.setModel(DbUtils.resultSetToTableModel(rs));
        }
        catch(SQLException e){
        JOptionPane.showMessageDialog(null, e);
        }
    }
    

